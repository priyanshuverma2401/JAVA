public class break_continue {
  public static void main(String[] args){
    int i = 0;
    //break statement
    while (true) { 
        System.out.println(i);
        i++;
        if (i>5) break;
    }
  // continue statement
  int k = 0;
  while (true) { 
      System.out.println(k);
      k++;
      if(k==3) continue;
      if(k>5) break;
  }}
  } 

  

